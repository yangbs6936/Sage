def normalize_locale(locale: str | None) -> str:
    if not locale:
        return DEFAULT_LOCALE

    normalized = locale.strip().replace("_", "-").lower()
    if normalized.startswith("en"):
        return "en-US"
    if normalized.startswith("zh"):
        return "zh-CN"
    return DEFAULT_LOCALE


def _match_supported_locale(locale: str | None) -> str | None:
    if not locale:
        return None

    normalized = locale.strip().replace("_", "-").lower()
    if normalized.startswith("en"):
        return "en-US"
    if normalized.startswith("zh"):
        return "zh-CN"
    return None


def locale_from_accept_language(accept_language: str | None) -> str:
    if not accept_language:
        return DEFAULT_LOCALE

    for item in accept_language.split(","):
        locale = item.split(";", 1)[0].strip()
        resolved = _match_supported_locale(locale)
        if resolved:
            return resolved
    return DEFAULT_LOCALE


def locale_from_request(request: Request | None) -> str:
    if request is None:
        return DEFAULT_LOCALE
    explicit_locale = request.headers.get("X-Sage-Locale")
    if explicit_locale:
        return normalize_locale(explicit_locale)
    return locale_from_accept_language(request.headers.get("Accept-Language"))


def has_message(key: str, locale: str | None = None) -> bool:
    resolved_locale = normalize_locale(locale)
    return key in MESSAGES.get(resolved_locale, {}) or key in MESSAGES[DEFAULT_LOCALE]


